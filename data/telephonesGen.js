var telephones = [ "202-555-0125", "202-555-0152", "202-555-0196", "202-555-0161", "202-555-0172", "202-555-0110",
                    "+1-202-555-0125", "+1-202-555-0152", "+1-202-555-0196", "+1-202-555-0161", "+1-202-555-0172",
                    "+1-202-555-0110", "202-555-0134", "202-555-0102", "202-555-0189", "202-555-0189", "202-555-0124",
                    "202-555-0165", "+1-202-555-0134", "+1-202-555-0102", "+1-202-555-0189", "+1-202-555-0189",
                    "+1-202-555-0124", "+1-202-555-0165" ];

exports.getRandomTel = function ( numFakes ) {
  var randomTels = [];

  for( i=0; i<numFakes; i++ ) {
    var randomTel = Math.floor( ( Math.random() * telephones.length ) + 1 );
    randomTels.push( telephones[ randomTel ] );
  }

  return randomTels;
};